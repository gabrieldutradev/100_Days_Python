# Arredondar numeros
print(int(8/3))
print(8/3)
print(round(8/3))
print(round(8/3, 2))
print(round(8/3, 4))
print(round(8/3, 1))
print(round(8/3, 0))

# Cortar o arredondamento, pegando o menor valor, floor division
